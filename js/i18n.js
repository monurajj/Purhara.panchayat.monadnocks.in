function getLang() {
  return localStorage.getItem('lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang === 'hi' ? 'hi' : 'en';
}

// Apply saved language to <html lang> on every page load
setLang(getLang());

function t(key) {
  const lang = getLang();
  const keys = key.split('.');
  let val = I18N[lang];
  for (const k of keys) {
    val = val?.[k];
  }
  return val ?? key;
}

/** English string for PDF export (Helvetica cannot render Devanagari). */
function tEn(key) {
  const keys = key.split('.');
  let val = I18N.en;
  for (const k of keys) {
    val = val?.[k];
  }
  return val ?? key;
}

function field(obj, fieldName) {
  if (!obj) return '';
  const lang = getLang();
  if (lang === 'hi' && obj[fieldName + '_hi']) return obj[fieldName + '_hi'];
  return obj[fieldName] ?? '';
}

function formatCurrency(amount) {
  const lang = getLang();
  const formatted = Number(amount).toLocaleString(lang === 'hi' ? 'hi-IN' : 'en-IN');
  return '₹' + formatted;
}

function formatNumber(n) {
  const lang = getLang();
  return Number(n).toLocaleString(lang === 'hi' ? 'hi-IN' : 'en-IN');
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    const text = t(key);
    if (text !== undefined && text !== null && text !== key) {
      el.textContent = text;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
  const toggle = document.getElementById('lang-toggle');
  if (toggle) toggle.textContent = t('langToggle');
  const pageTitleKey = document.body.dataset.pageTitleKey;
  const pageLabel = pageTitleKey ? t(pageTitleKey) : (document.body.dataset.pageTitle || '');
  document.title = pageLabel ? `${t('siteTitle')} | ${pageLabel}` : t('siteTitle');
  if (document.getElementById('about-title')) {
    applyAboutPage();
  }
}

